    private void jButtonGravarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonGravarActionPerformed
        // TODO add your handling code here:
        //Iniciando as classes modelo e controles
        if (txtCnpj.getText().equals("") && txtCnes.getText().equals("") && txtNomeFantasia.getText().equals("") && txtRazaoSocial.getText().equals("") && txtQtdLeitos.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Campos em negritos são obrigatórios.");
        } else {
            Instituicao i = new Instituicao();
            InstituicaoDAO idao = new InstituicaoDAO();

            //Setando Objeto
            i.setCnpj(Long.parseLong(this.limpaFormatacaoString(txtCnpj.getText().toString()))); //convertendo texto em inteiro
            i.setNomeFantasia(txtNomeFantasia.getText());
            i.setCnes(Long.parseLong(this.limpaFormatacaoString(txtCnes.getText()))); //convertendo texto em inteiro
            i.setRazaoSocial(txtRazaoSocial.getText());
            i.setNaturezaJuridica(txtNatureza.getText());
            i.setLogradouro(txtLogradouro.getText());
            i.setNumero(Integer.parseInt(txtNumero.getText())); //convertendo texto em inteiro
            i.setComplemento(txtComplemento.getText());
            i.setBairro(txtBairro.getText());
            i.setMunicipio(txtMunicipio.getText());
            i.setUf(txtUf.getText());
            i.setCep(Integer.parseInt(this.limpaFormatacaoString(txtCep.getText()))); //convertendo texto em inteiro
            i.setTelefone(Long.parseLong(this.limpaFormatacaoString(txtTelefone.getText()))); //convertendo texto em inteiro

            String escolhaTexto = cbTipo.getSelectedItem().toString(); //buscando escolha realizada

            i.setTipo(this.converterTipoEmCodigo(escolhaTexto));

            i.setQtdLeitos(Integer.parseInt(txtQtdLeitos.getText())); //convertendo texto em inteiro
            if (acao.equals("CADASTRO")) {

                //Gravando instituicao no BD
                boolean sucesso = idao.insereInstituicao(i);
                if (sucesso) {
                    JOptionPane.showMessageDialog(null, "Cadastro realizado com sucesso.");
                    this.limpatela();
                }
            }
            if (acao.equals("EDITAR")) {

                //Editando instituicao no BD
                boolean sucesso = idao.editarInstituicao(i);
                if (sucesso) {
                    JOptionPane.showMessageDialog(null, "Edição realizada com sucesso.");
                    this.limpatela();

                }

            }
        }
    }//GEN-LAST:event_jButtonGravarActionPerformed

