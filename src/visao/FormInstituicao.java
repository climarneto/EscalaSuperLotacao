    private void preencherTabelaInstituicoes(List<Instituicao> lista) {
        Iterator itr = lista.iterator();
        DefaultTableModel modelo = (DefaultTableModel) jTableInstituicoes.getModel();
        modelo.setNumRows(0);
        Object[] fila = new Object[3];
        while (itr.hasNext()) {
            Instituicao i = (Instituicao) itr.next();
            int j = 0;
            fila[j++] = i.getCnpj();
            fila[j++] = i.getNomeFantasia();
            fila[j++] = i.getRazaoSocial();
            modelo.addRow(fila);
        }

    }

    private void limparTabelaInstituicoes() {
        DefaultTableModel modelo = (DefaultTableModel) jTableInstituicoes.getModel();
        modelo.setNumRows(0);

    }

    private void popularTelaCadastro(Instituicao i) {
        txtCnpj.setEnabled(false);
        txtCnpj.setText(String.format("%014d", i.getCnpj()));
        txtNomeFantasia.setText(i.getNomeFantasia());
        txtRazaoSocial.setText(i.getRazaoSocial());
        txtCnes.setText(String.format("%07d", i.getCnes()));
        txtNatureza.setText(i.getNaturezaJuridica());
        txtLogradouro.setText(i.getLogradouro());
        txtNumero.setText(String.valueOf(i.getNumero()));
        txtComplemento.setText(i.getComplemento());
        txtBairro.setText(i.getBairro());
        txtMunicipio.setText(i.getMunicipio());
        txtUf.setText(i.getUf());
        txtCep.setText(String.format("%08d", i.getCep()));
        txtTelefone.setText(i.getTelefone().toString());

        cbTipo.setSelectedIndex(this.converterCodigoTipoEmDescricao(i.getTipo()));
        txtQtdLeitos.setText(String.valueOf(i.getQtdLeitos()));
        //setando os botões
        jButtonBuscar.setEnabled(false);
        jButtonGravar.setEnabled(true);
        jButtonExcluir.setEnabled(true);
        jButtonCancelar.setEnabled(true);
        acao = "EDITAR";
    }

    private int converterCodigoTipoEmDescricao(String codigo) {
        int retorno = 0;
        if (codigo.equals("P")) {
            retorno = 0;
        }
        if (codigo.equals("H")) {
            retorno = 1;
        }

        return retorno;

    }

    private String converterTipoEmCodigo(String escolhaTexto) {
        String escolhaCodigo = "";
        if (escolhaTexto == "Pronto Atendimento") {
            escolhaCodigo = "P";
        }
        if (escolhaTexto == "Hospital") {
            escolhaCodigo = "H";
        }
        return escolhaCodigo;
    }

    private String limpaFormatacaoString(String texto) {
        String textoLimpo = texto;
        //limpando -
        textoLimpo = texto.replace("-", "");
        texto = textoLimpo;
        //limpando .
        textoLimpo = texto.replace(".", "");
        texto = textoLimpo;

        //limpando (
        textoLimpo = texto.replace("(", "");
        texto = textoLimpo;

        //limpando )
        textoLimpo = texto.replace(")", "");
        texto = textoLimpo;

        //limpando )
        textoLimpo = texto.replace("/", "");
        texto = textoLimpo;

        return textoLimpo;

    }
