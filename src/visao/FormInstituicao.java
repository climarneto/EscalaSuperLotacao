    private void jTableInstituicoesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableInstituicoesMouseClicked
        // TODO add your handling code here:
        if (evt.getClickCount() == 2) {
            Long cnpj = (Long) jTableInstituicoes.getValueAt(jTableInstituicoes.getSelectedRow(), 0);

            InstituicaoDAO idao = new InstituicaoDAO();
            Instituicao i = new Instituicao();
            i = idao.buscaInstituicaoPorCnpj(cnpj);

            this.popularTelaCadastro(i);
            this.limparTabelaInstituicoes();
            txtPesquisar.setText(null);

            jPanelPesquisar.setVisible(false);
            jPanelCadastro.setVisible(true);
            txtNomeFantasia.requestFocus();

        }
    }//GEN-LAST:event_jTableInstituicoesMouseClicked

