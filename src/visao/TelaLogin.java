    private void jButtonAlterarSenhaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAlterarSenhaActionPerformed
        // TODO add your handling code here:
        String usuario = jTextFieldUsuario.getText();
        String senhaNova = jPasswordFieldSenhaNova.getText();
        String repeticao = jPasswordFieldRepeticao.getText();
        if(senhaNova == null ? repeticao == null : senhaNova.equals(repeticao)) {
            UsuarioDAO udao = new UsuarioDAO();
            boolean sucesso = false;
            try {
                sucesso = udao.atualizaSenha(usuario, senhaNova);
            } catch (BadPaddingException ex) {
                Logger.getLogger(TelaLogin.class.getName()).log(Level.SEVERE, null, ex);
            } catch (NoSuchPaddingException ex) {
                Logger.getLogger(TelaLogin.class.getName()).log(Level.SEVERE, null, ex);
            } catch (IllegalBlockSizeException ex) {
                Logger.getLogger(TelaLogin.class.getName()).log(Level.SEVERE, null, ex);
            } catch (InvalidKeyException ex) {
                Logger.getLogger(TelaLogin.class.getName()).log(Level.SEVERE, null, ex);
            } catch (NoSuchAlgorithmException ex) {
                Logger.getLogger(TelaLogin.class.getName()).log(Level.SEVERE, null, ex);
            } catch (InvalidAlgorithmParameterException ex) {
                Logger.getLogger(TelaLogin.class.getName()).log(Level.SEVERE, null, ex);
            }
            if (sucesso){
                JOptionPane.showMessageDialog(rootPane, "Senha alterada com sucesso!");
                jPanelAlteraSenha.setVisible(false);
                jPasswordFieldSenha.setText(null);
                jPanelLogon.setVisible(true);
                jPasswordFieldSenha.requestFocus();
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "Senhas não conferem!");
        }
    }//GEN-LAST:event_jButtonAlterarSenhaActionPerformed

