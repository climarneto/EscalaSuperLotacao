    private void jButtonAcessarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAcessarActionPerformed
        Usuario u = new Usuario();
        UsuarioDAO udao = new UsuarioDAO();
        String usuario = jTextFieldUsuario.getText();
        String senhaDigitada = jPasswordFieldSenha.getText();
        String senhaBanco = null;
        String senhaDigitadaCriptografada = null;
        try {
            senhaBanco = udao.buscaSenha(usuario);
        } catch (BadPaddingException ex) {
            Logger.getLogger(TelaLogin.class.getName()).log(Level.SEVERE, null, ex);
        } catch (NoSuchPaddingException ex) {
            Logger.getLogger(TelaLogin.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IllegalBlockSizeException ex) {
            Logger.getLogger(TelaLogin.class.getName()).log(Level.SEVERE, null, ex);
        } catch (InvalidKeyException ex) {
            Logger.getLogger(TelaLogin.class.getName()).log(Level.SEVERE, null, ex);
        } catch (NoSuchAlgorithmException ex) {
            Logger.getLogger(TelaLogin.class.getName()).log(Level.SEVERE, null, ex);
        } catch (InvalidAlgorithmParameterException ex) {
            Logger.getLogger(TelaLogin.class.getName()).log(Level.SEVERE, null, ex);
        }
        try {
            //Descriptografando senha do Banco
            senhaDigitadaCriptografada = SenhaBanco.encrypt(senhaDigitada);
        } catch (BadPaddingException ex) {
            Logger.getLogger(TelaLogin.class.getName()).log(Level.SEVERE, null, ex);
        } catch (NoSuchPaddingException ex) {
            Logger.getLogger(TelaLogin.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IllegalBlockSizeException ex) {
            Logger.getLogger(TelaLogin.class.getName()).log(Level.SEVERE, null, ex);
        } catch (InvalidKeyException ex) {
            Logger.getLogger(TelaLogin.class.getName()).log(Level.SEVERE, null, ex);
        } catch (NoSuchAlgorithmException ex) {
            Logger.getLogger(TelaLogin.class.getName()).log(Level.SEVERE, null, ex);
        } catch (InvalidAlgorithmParameterException ex) {
            Logger.getLogger(TelaLogin.class.getName()).log(Level.SEVERE, null, ex);
        }

        //Conferindo se Senha Digitada confere com o banco de dados
        if (senhaDigitadaCriptografada == null ? senhaBanco == null : senhaDigitadaCriptografada.equals(senhaBanco)) {
            boolean provisorio = false;
            try {
                provisorio = udao.checaSenhaProvisoria(usuario);
            } catch (BadPaddingException ex) {
                Logger.getLogger(TelaLogin.class.getName()).log(Level.SEVERE, null, ex);
            } catch (NoSuchPaddingException ex) {
                Logger.getLogger(TelaLogin.class.getName()).log(Level.SEVERE, null, ex);
            } catch (IllegalBlockSizeException ex) {
                Logger.getLogger(TelaLogin.class.getName()).log(Level.SEVERE, null, ex);
            } catch (InvalidKeyException ex) {
                Logger.getLogger(TelaLogin.class.getName()).log(Level.SEVERE, null, ex);
            } catch (NoSuchAlgorithmException ex) {
                Logger.getLogger(TelaLogin.class.getName()).log(Level.SEVERE, null, ex);
            } catch (InvalidAlgorithmParameterException ex) {
                Logger.getLogger(TelaLogin.class.getName()).log(Level.SEVERE, null, ex);
            }
            if (provisorio) {
                JOptionPane.showMessageDialog(rootPane, "Senha expirada! Favor alterá-la.");
                jPanelAlteraSenha.setVisible(true);
                jPanelLogon.setVisible(false);
                jPasswordFieldSenhaNova.requestFocus();
            } else {
                TelaPrincipal tela = new TelaPrincipal();
                tela.setVisible(true);
                dispose();
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "Usuário e/ou Senha inválidos!");
            jPasswordFieldSenha.setText(null);
            jPasswordFieldSenha.requestFocus();
        }

// TODO add your handling code here:
    }//GEN-LAST:event_jButtonAcessarActionPerformed

